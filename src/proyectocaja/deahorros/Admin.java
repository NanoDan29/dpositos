    public void buscarDir() {
        File url = new File(ubicacion + txtdir.getText() + ".txt");

        if (txtdir.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese un directorio");
        } else {
            if (url.exists()) {
                try {

                    //Abrimos el archivo 
                    FileInputStream fis = new FileInputStream(url);
                    Properties mostrar = new Properties();
                    mostrar.load(fis);

                    System.out.println(mostrar.getProperty("Id"));
                    System.out.println(mostrar.getProperty("Descripcion"));

                } catch (Exception e) {
                }
            } else {
                JOptionPane.showMessageDialog(null, "Este registro no existe");
            }
        }

    }


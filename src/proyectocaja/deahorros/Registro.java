    private void crear() {
        String nombreArchivo = txtCedula.getText() + ".txt";
        File crearUbicacion = new File(ubicacion);
        File crearArchivo = new File(ubicacion + nombreArchivo);

        if (txtCedula.getText().equals("")
                || txtDireccion.getText().equals("")
                || txtCAhorro.getText().equals("")
                || txtNombres.getText().equals("")
                || txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Por favor llene todos los campos");
        } else {
            try {
                if (crearArchivo.exists()) {
                    JOptionPane.showMessageDialog(rootPane, "Este Registro ya existe");
                } else {
                    crearUbicacion.mkdir();
                    Formatter crea = new Formatter(ubicacion + nombreArchivo);
                    crea.format("%s\r\n%s\r\n%s\r\n%s\r\n%s",
                            "Id=" + txtCedula.getText(),
                            "Nombre=" + txtNombres.getText(),
                            "Telefono=" + txtTelefono.getText(),
                            "Direccion=" + txtDireccion.getText(),
                            "CuotaAhorro=" + txtCAhorro.getText());

                    crea.close();
                    JOptionPane.showMessageDialog(rootPane, "Este Registro ha sido creado");

                    //Nos dirigimos al frame de login
                    Login abrir = new Login();
                    abrir.setVisible(true);
                    abrir.setLocationRelativeTo(null);
                    this.setVisible(false);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "No se pudo crear");
            }

        }
    }

